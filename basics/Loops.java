// import java.util.*;
public class Loops {
    public static void main(String args[]){
        //for
        for (int i=1; i<=20; i++) {
            System.out.println(i);
        }
        //while
        int i = 0;
        while(i<=20) {
            System.out.println(i);
            i++;
        }
        //do while
        int n = 0;
        do {
            System.out.println(n);
            n++;
        } while(n==20);
           
    }
}
